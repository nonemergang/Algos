# Расчет площади треугольника по формуле Герона

print("Эта программа вычисляет площадь треугольника по формуле Герона")
print("по трем сторонам a, b, c")
print()

# Ввод данных
try:
    a = float(input("Введите длину стороны a: "))
    b = float(input("Введите длину стороны b: "))
    c = float(input("Введите длину стороны c: "))
except ValueError:
    print("Ошибка: введите числовые значения для сторон треугольника")
    exit()

# Валидация данных
if a <= 0 or b <= 0 or c <= 0:
    print("Ошибка: все стороны треугольника должны быть положительными числами")
    exit()

if a + b <= c or a + c <= b or b + c <= a:
    print("Ошибка: такие стороны не могут образовать треугольник")
    print("(сумма любых двух сторон должна быть больше третьей)")
    exit()

# Вычисление по формуле Герона
p = (a + b + c) / 2  # полупериметр
area = (p * (p - a) * (p - b) * (p - c)) ** 0.5

# Вывод результата
print()
print(f"Результаты расчета:")
print(f"Сторона a = {a}")
print(f"Сторона b = {b}")
print(f"Сторона c = {c}")
print(f"Площадь треугольника = {area:.2f}")